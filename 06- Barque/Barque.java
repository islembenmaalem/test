package tp3;

public class Barque {
	private String nom;
	private String num;

	public Barque(String nom, String num) {
		this.nom = nom;
		this.num = num;
	}

	public String toString() { 
		return "\n\tnom = " + nom + " numero = " + num;
	}

	public String getNum() {
		return num;
	}

}











